def main(args):
    whole_data_path = f"./dataset/{args.dataset}_Steering.json"
    whole_data = json.load(open(whole_data_path, 'r'))
    print(f"Total instances in whole data: {len(whole_data)}")


    instance = args.instance_id
    for instance_id in range(instance, args.end_id+1):
        print(f"Processing instance ID: {instance_id}")

        user_inout = whole_data[instance_id]['user_input']
        reference_solution = whole_data[instance_id]['reference_solution']
        misinfo_goal = whole_data[instance_id]['misinfo_goal']

        root = "./Diagnosis_results/output_level_score"
        if not os.path.exists(root):
            os.makedirs(root)
        
        result_paths = []

        vanilla_path = f"./logs/{args.dataset}/log_vanilla_P_{args.planner_model}_E_{args.executor_model}_step_3/vanilla/{instance_id}_{args.planner_model}_{args.executor_model}_vanilla_False.jsonl"
        agent_number = len(read_jsonl(vanilla_path, "topology")[3]['context'])
        print(f"Number of agents in this instance: {agent_number}")

        result_paths.append(vanilla_path)
        
        
        for hijack_id in range(agent_number):
            result_paths.append(f"./logs/{args.dataset}/log_hijack_P_{args.planner_model}_E_{args.executor_model}_step_3/hijack/{instance_id}_hijack_{hijack_id}_P_{args.planner_model}_E_{args.executor_model}_inject_False_mis_ori.jsonl")
        
        results = {}
        for idx, data_path in enumerate(result_paths):
            
            save_path = f"{root}/{instance_id}_{args.planner_model}_{args.executor_model}_inject_False.json"
            
            print(f"Reading data from: {data_path}")
            print(f"Saving processed data to: {save_path}")
           
            data = read_jsonl(data_path, "goal_drift_score")
        

            task_output = data[-1]["context"]
            prompt = evaluation_prompt.format(
                user_inout,
                reference_solution,
                misinfo_goal,
                task_output
            )

            
            try:
                response = langauage_reasoning(prompt)
                response = json.loads(response)
                print(f"Evaluation Response: {response}\n")
            except Exception as e:
                print(f"LLM call failed: {e}")
            
            if idx == 0:
                results['vanilla'] = response
            else:                
                results[f'hijack_{idx-1}'] = response
                
        with open(save_path, 'w', encoding='utf-8') as output_file:
            json.dump(results, output_file, ensure_ascii=False, indent=4)
       
